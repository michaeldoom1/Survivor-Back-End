# Real people who play across multiple seasons under the same name.
# Resolved with the league owner by hand - a shared name is NOT enough to
# assume it's the same person (e.g. "Chrissy" and "Stephanie" below are
# coincidentally different people in seasons 48 vs. 50).
shared_people_data = [
  { name: "Joe", gender: "Male", seasons: [ 48, 50 ] },
  { name: "Genevieve", gender: "Female", seasons: [ 47, 50 ] },
  { name: "Savannah", gender: "Female", seasons: [ 49, 50 ] },
  { name: "Kyle", gender: "Male", seasons: [ 48, 50 ] }, # Season 47's Kyle is a different person
  { name: "Kamilla", gender: "Female", seasons: [ 48, 50 ] },
  { name: "Rizo", gender: "Male", seasons: [ 49, 50 ] },
  { name: "Charlie", gender: "Male", seasons: [ 46, 50 ] },
  { name: "Q", gender: "Male", seasons: [ 46, 50 ] },
  { name: "Tiffany", gender: "Female", seasons: [ 46, 50 ] }
]

shared_person_for = {}
shared_people_data.each do |data|
  person = Person.find_or_create_by!(name: data[:name], gender: data[:gender])
  data[:seasons].each { |season_number| shared_person_for[[ season_number, data[:name] ]] = person }
end

seasons_data.each do |number, contestants|
  season = Season.find_or_create_by!(number: number)

  contestants.each do |attrs|
    person = shared_person_for[[ number, attrs[:name] ]]
    person ||= season.contestants.joins(:person).find_by(people: { name: attrs[:name] })&.person
    person ||= Person.create!(name: attrs[:name], gender: attrs[:gender])

    Contestant.find_or_create_by!(season: season, person: person)
  end
end

